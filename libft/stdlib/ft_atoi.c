#include "../libft.h"

int	ft_atoi(const char *str)
{
	int	ret;
	int	i;
	int	neg;

	ret = 0;
	i = 0;
	neg = 1;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret *= 10;
		ret += (str[i] - '0');
		i++;
	}
	return (neg * ret);
}
